const mongoose = require('mongoose');

const DataSchena = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    role: {
        type: String,
        required: true
    }
});

const users = mongoose.model('users',  DataSchena);
module.exports = users;